char *i2c_if = "/dev/i2c-1";

optiga_comms_t optiga_comms =
{
    (void*)&ifx_i2c_context_0,
    NULL,
    NULL,
    0
};

VAULT_INFINEON_CFG_s optiga_trust_x_cfg = {
    .iface                      = VAULT_INFINEON_IFACE_I2C,
    .iface_cfg                  = &optiga_comms,
};

OCKAM_VAULT_CFG_s vault_cfg =
{
    .p_tpm                       = &optiga_trust_x_cfg,
    .p_host                      = 0,
    OCKAM_VAULT_EC_P256
};


