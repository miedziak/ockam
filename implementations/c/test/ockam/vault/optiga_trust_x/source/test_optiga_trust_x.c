/**
 ********************************************************************************************************
 *                                             main()
 *
 * @brief   Main point of entry for Optiga Trust X test
 ********************************************************************************************************
 */

void main (void)
{
    OCKAM_ERR err;
    uint8_t i;


    /* ---------- */
    /* Vault Init */
    /* ---------- */

    err = ockam_vault_init((void*) &vault_cfg);                 /* Initialize vault for Optiga Trust X and mbedcrypto */
    if(err != OCKAM_ERR_NONE) {
        test_vault_print(OCKAM_LOG_ERROR,
                         "OPTIGA TRUST X",
                          0,
                         "Error: Ockam Vauilt Init failed");
        return;
    }

    /* ------------------------ */
    /* Random Number Generation */
    /* ------------------------ */

    test_vault_random();

    /* --------------------- */
    /* Key Generation & ECDH */
    /* --------------------- */

    test_vault_key_ecdh(vault_cfg.ec);

    /* ------ */
    /* SHA256 */
    /* ------ */

    test_vault_sha256();

    /* -----*/
    /* HKDF */
    /* -----*/

    test_vault_hkdf();

    /* -------------------- */
    /* AES GCM Calculations */
    /* -------------------- */

    test_vault_aes_gcm();

    return;
}

